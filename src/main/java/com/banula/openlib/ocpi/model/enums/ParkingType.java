package com.banula.openlib.ocpi.model.enums;

public enum ParkingType {
    /**
     * Location on a parking facility/rest area along a motorway, freeway,
     * interstate, highway etc.
     */
    ALONG_MOTORWAY,
    /**
     * Multistorey car park.
     */
    PARKING_GARAGE,
    /**
     * A cleared area that is intended for parking vehicles, i.e. at super markets,
     * bars, etc.
     */
    PARKING_LOT,
    /**
     * Location is on the driveway of a house/building.
     */
    ON_DRIVEWAY,
    /**
     * Parking in public space along a street.
     */
    ON_STREET,
    /**
     * Multistorey car park, mainly underground.
     */
    UNDERGROUND_GARAGE
}
